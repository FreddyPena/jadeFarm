    private void save() {
        if (!validateField()) {
            return;
        }

        int men = JOptionPane.showConfirmDialog(this, articulo == null ? "Desea guardar el nuevo registro?"
                : "Desea guardar los cambios?", null, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (men == JOptionPane.NO_OPTION) {
            return;
        }

        if (articulo == null) {
            articulo = new Articulo();
        }

        articulo.setDescripcion(jtDescripcion.getText().trim());
        articulo.setReferencia(jtReferencia.getText().trim());
        articulo.setCodigoBarra(jtCBarra.getText().trim());
        articulo.setIngredienteActivo(cbIngActivo.getSelectedItem());
        articulo.setFabricante(cbFabricante.getSelectedItem());
        articulo.setFamilia(ManejoFamilia.getInstance().findIdentifier(jtIdenFamilia.getText().trim()));
        articulo.setMarca(ManejoMarca.getInstance().findIdentifier(jtIdenMarca.getText().trim()));
        articulo.setModelo(ManejoModelo.getInstance().identifier(jtIdenModelo.getText().trim()));
        articulo.setRenglon(cbRenglon.getSelectedItem());
        articulo.setClasArticulo(cbClasificacion.getSelectedItem());
        if (ckActivo.isSelected()) {
            articulo.setActivo("A");
        } else if (!ckActivo.isSelected()) {
            articulo.setActivo("I");
        }
        if (ckPermiteDesc.isSelected()) {
            articulo.setPermiteDescuento("1");
        } else if (!ckPermiteDesc.isSelected()) {
            articulo.setActivo("0");
        }
        if (ckPermiteVenc.isSelected()) {
            articulo.setPermiteDescuento("1");
        } else if (!ckPermiteVenc.isSelected()) {
            articulo.setActivo("0");
        }
        if (ckExentoItbis.isSelected()) {
            articulo.setPermiteDescuento("1");
        } else if (!ckExentoItbis.isSelected()) {
            articulo.setActivo("0");
        }
        if (ckVentaDetalle.isSelected()) {
            articulo.setPermiteDescuento("1");
        } else if (!ckVentaDetalle.isSelected()) {
            articulo.setActivo("0");
        }
        if (rbMBeneficio.isSelected()) {
            articulo.setCalculoPrecio(ManejoArticulo.getCalculoPrecio("1"));
        } else if (rbPDirecto.isSelected()) {
            articulo.setCalculoPrecio(ManejoArticulo.getCalculoPrecio("2"));
        }
        articulo.setPresentaciones(tbPresentacion.getElements());
        ManejoArticulo.getInstance().create(articulo);
        changeDisplacement = true;
        clean();
        fireTableDataChanged();
        setTablePanel(1, true);
        setToolBar(true);
    }

