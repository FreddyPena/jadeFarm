    private void setElement(Articulo articulo) {
        selectRow();
        if (articulo != null) {
            this.articulo = articulo;
            jtReferencia.setText(articulo.getReferencia());
            jtCBarra.setText(articulo.getCodigoBarra());
            jtDescripcion.setText(articulo.getDescripcion());
            cbIngActivo.setValue(articulo.getIngredienteActivo());
            cbFabricante.setValue(articulo.getFabricante());
            tbPresentacion.setElements(articulo.getPresentaciones());
            jtIdenFamilia.setText(articulo.getFamilia().getIdentificador());
            jtDescFamilia.setText(articulo.getFamilia().getDescripcion());
            jtIdenMarca.setText(articulo.getMarca().getIdentificador());
            jtDescMarca.setText(articulo.getMarca().getDescripcion());
            jtIdenModelo.setText(articulo.getModelo().getIdentificador());
            jtDescModelo.setText(articulo.getModelo().getDescripcion());
            cbRenglon.setValue(articulo.getRenglon());
            cbClasificacion.setValue(articulo.getClasArticulo());
            if (articulo.getCalculoPrecio() != null) {
                switch (ManejoArticulo.getCalculoPrecio(articulo.getCalculoPrecio())) {
                    case "1":
                        rbMBeneficio.setSelected(true);
                        break;
                    case "2":
                        rbPDirecto.setSelected(true);
                        break;
                }
            }
            if (articulo.getActivo() != null) {
                switch (articulo.getActivo()) {
                    case "A":
                        ckActivo.setSelected(true);
                        break;
                    case "I":
                        ckActivo.setSelected(false);
                        break;
                }
            }
            if (articulo.getExentoItbis() != null) {

                switch (articulo.getExentoItbis()) {
                    case "1":
                        ckExentoItbis.setSelected(true);
                        break;
                    case "0":
                        ckExentoItbis.setSelected(false);
                        break;
                }
            }
            if (articulo.getPermiteDescuento() != null) {
                switch (articulo.getPermiteDescuento()) {
                    case "1":
                        ckPermiteDesc.setSelected(true);
                        break;
                    case "0":
                        ckPermiteDesc.setSelected(false);
                        break;
                }
            }
            if (articulo.getPermiteVencimiento() != null) {
                switch (articulo.getPermiteVencimiento()) {
                    case "1":
                        ckPermiteVenc.setSelected(true);
                        break;
                    case "0":
                        ckPermiteVenc.setSelected(false);
                        break;
                }
            }
            if (articulo.getVentaDetallado() != null) {
                switch (articulo.getVentaDetallado()) {
                    case "1":
                        ckVentaDetalle.setSelected(true);
                        break;
                    case "0":
                        ckVentaDetalle.setSelected(false);
                        break;
                }
            }
            if (articulo.getPresentaciones() != null) {
                tbPresentacion.setElements(articulo.getPresentaciones());
            }
            objectDisplacement.setRegistroActual(articulo);
        }
    }

    private void selectRow() {

        if (articulo != null) {
            objectDisplacement.setRegistroActual(articulo);
            if (jtpStandard.getSelectedIndex() == 1) {

                int row = 0;
                for (Articulo f : tbArticulos.getElements()) {
                    if (f.equals(articulo)) {
                        tbArticulos.changeSelection(row, 1, false, false);
                        return;
                    }
                    row++;
                }
                return;
            }
        }

        if (jtpStandard.getSelectedIndex() == 1 && tbArticulos.getSelectedRow() == -1) {
            tbArticulos.changeSelection(0, 1, false, false);
            objectDisplacement.setLista(tbArticulos.getElements());
        }

    }

