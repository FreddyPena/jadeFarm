    private void tbArticulosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbArticulosMouseClicked
        objectDisplacement.setLista(tbArticulos.getElements());
        if (evt.getClickCount() == 2) {
            articulo = ManejoArticulo.getInstance().find(tbArticulos.getSelectedElement().getCodigo());
            articulo.setPresentaciones(ManejoArticuloPresentacion.getInstance().listItemPresentation(articulo));
            setElement(articulo);
            if (!consultation) {
                setTablePanel(0, false);
                setToolBar(false);
            } else {
                dispose();
            }
        }
    }//GEN-LAST:event_tbArticulosMouseClicked

